CREATE TABLE IF NOT EXISTS adotante(
    id SERIAL PRIMARY KEY,
	CPF VARCHAR(11) NOT NULL ,
	RG VARCHAR(20) NOT NULL,
	orgao_RG VARCHAR(10) NOT NULL,
	nome VARCHAR(100) NOT NULL,
	data_nasc DATE NOT NULL CHECK (data_nasc <= CURRENT_DATE - INTERVAL '21 years'),
	profissao VARCHAR(50),
	renda_mensal NUMERIC(10,2) CHECK (renda_mensal >= 0),
	estado_civil VARCHAR(25) CHECK (estado_civil IN ('SOLTEIRO','CASADO','DIVORCIADO','VIUVO','SEPARADO_JUDICIALMENTE')),
	escolaridade VARCHAR(50),
	telefone1 VARCHAR(15) NOT NULL,
	telefone2 VARCHAR(15),
	email VARCHAR(100) UNIQUE,
	instagram VARCHAR(50),
	logradouro VARCHAR(100) NOT NULL,
	bairro VARCHAR(50) NOT NULL,
	cidade VARCHAR(50) NOT NULL,
	estado VARCHAR(2) NOT NULL,
	CEP VARCHAR(8) NOT NULL,
	num_endereco VARCHAR(10) NOT NULL,
	complemento VARCHAR(50)
);